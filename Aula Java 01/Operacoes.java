public class Operacoes {
    public static void main(String[] args){
        double valor = 10;

        valor += 10;            
        System.out.println(valor);
    
        valor -= 15;
        System.out.println(valor);

        valor *= 2;            
        System.out.println(valor);

        valor /= 10;    
        System.out.println(valor);
    }      
}
